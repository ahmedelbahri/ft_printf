#include "ft_printf.h"

void	ft_putstr(char *s, int *len)
{
	if (!s)
	{
		write(1, "(null)", 6);
		(*len) = (*len) + 6;
	}
	else
	{
		(*len) = (*len) + ft_strlen(s);
		write(1, s, ft_strlen(s));
	}
}
